#include <stdio.h>

/**
 * main - prints all possible combinations of single-digit numbers in ascending order
 * Return: Eachtime 0 (Success)
 */
int main(void)
{
	int n;

	for (n = 48; n < 58; n++)
	{
		putchar(n);
		if (n != 57)
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');
	return (0);
}
